One Night in Bejing is an annual cultural performance put on by Awareness of Roots in Chinese Culture.  Since spring of 2005, One Night in Beijing has provided audience members with an immersing experience of Chinese dance, music, tradition and food.  In past years, performances have included:

<ul>
<li> Fan and ribbon dances, showcasing a brilliant display of colors and sound </li>
<li> Dazzling fashion shows, showcasing a never ending list of traditional and modern wear from the four corners of China </li>
<li> Heart pumping modern dances, showing the hip hop side of China </li>
<li> Lion dance, an acrobatic and rhythmic traditional dance with elaborate costumes </li>
<li> Fusion dances, merging traditional Chinese fan and ribbons with modern themes </li>
<li> A variety of bands and vocalists performing current popular Chinese music </li>
<li> A comedic and entertaining skit, spanning the duration of the show. </li>
</ul>

One Night in Beijing is a variety show that has something for everyone.  It will transport you to the heart of China and immerse you in a truly unforgettable and entertaining experience.

